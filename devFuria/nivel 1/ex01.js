function adição(n1, n2){
    var result = n1 + n2
    return console.log("Resultado adição: " + result)
}

function subtração(n1, n2){
    var result = n1 - n2
    return console.log("Resultado subtração: " + result)
}

function multiplicação(n1, n2){
    var result = n1 * n2
    return console.log("Resultado multiplicação: " + result)
}

function divisao(n1, n2){
    var result = n1 / n2
    return console.log("Resultado divisao: " + result)
}

